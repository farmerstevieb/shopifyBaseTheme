/**
 * generate-theme-assets.js
 * Generates dist/snippets/theme_assets.liquid after webpack build.
 * This snippet bootstraps the webpack runtime on Shopify by:
 *   1. Setting the CDN public path so lazy chunks load correctly
 *   2. Loading main.min.css and main.min.js via Liquid asset_url
 */

const fs = require("fs");
const path = require("path");

const OUTPUT = path.join(__dirname, "../dist/snippets/theme_assets.liquid");

const content = `{% # theme-check-disable %}
{%- comment -%}
  Loads webpack-bundled assets for the theme.
  Auto-generated by scripts/generate-theme-assets.js — do not edit directly.

  Usage:
  {%- render 'theme_assets', import_map: true -%}
  {%- render 'theme_assets' with 'main.min.js', preload: true, is_module: false -%}
  {%- render 'theme_assets' with 'main.min.css', preload: true -%}
{%- endcomment -%}

{%- if import_map == true -%}
  {%- assign _js_url = 'main.min.js' | asset_url -%}
  {%- assign _public_path = _js_url | split: 'main.min.js' | first -%}
  <script data-public-path="{{ _public_path }}">{}</script>

{%- elsif theme_assets == 'main.min.js' -%}
  {%- if preload -%}
    <link rel="preload" href="{{ 'main.min.js' | asset_url }}" as="script">
  {%- endif -%}
  {%- if is_module -%}
    <script type="module" src="{{ 'main.min.js' | asset_url }}"></script>
  {%- else -%}
    <script defer src="{{ 'main.min.js' | asset_url }}"></script>
  {%- endif -%}

{%- elsif theme_assets == 'main.min.css' -%}
  {%- if preload -%}
    <link rel="preload" href="{{ 'main.min.css' | asset_url }}" as="style">
  {%- endif -%}
  <link rel="stylesheet" href="{{ 'main.min.css' | asset_url }}">

{%- elsif theme_assets != blank -%}
  <link rel="stylesheet" href="{{ theme_assets | asset_url }}">

{%- endif -%}
`;

fs.writeFileSync(OUTPUT, content);
console.log("✓ Generated theme_assets.liquid");
