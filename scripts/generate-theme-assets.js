/**
 * generate-theme-assets.js
 *
 * Generates dist/snippets/theme_assets.liquid after each webpack build.
 *
 * The snippet handles three cases:
 *   1. render 'theme_assets', import_map: true
 *      → Outputs a <script data-public-path> containing a JSON map of chunk
 *        filenames → their Shopify CDN query strings (?v=xxx). This allows
 *        webpack's runtime (public-path.ts) to locate and load async chunks
 *        from the Shopify CDN.
 *
 *   2. render 'theme_assets' with 'main.min.css', preload: true
 *      → Outputs <link rel="preload"> + <link rel="stylesheet">
 *
 *   3. render 'theme_assets' with 'main.min.js', preload: true, is_module: false
 *      → Outputs <link rel="preload"> + <script defer> (or type="module")
 */

const fs = require("fs");
const path = require("path");

const assetsDir = path.resolve(__dirname, "../dist/assets");
const snippetsDir = path.resolve(__dirname, "../dist/snippets");

// These are loaded directly by theme.liquid or are not webpack chunks.
const DIRECT_LOAD = new Set([
  "main.min.js",
  "main.min.css",
  "main.min.js.LICENSE.txt",
  "side-effects.js",
  "shop-the-look.js",
  "shop-the-look.css",
  "slider.js",
  "slider.css",
  "lazyload.js",
]);

const NON_CHUNK_EXTS = new Set([".png", ".jpg", ".jpeg", ".gif", ".svg", ".webp", ".woff", ".woff2", ".ttf", ".eot", ".txt"]);

const allFiles = fs.readdirSync(assetsDir);
const chunkFiles = allFiles
  .filter((f) => {
    const ext = path.extname(f);
    return !NON_CHUNK_EXTS.has(ext) && !DIRECT_LOAD.has(f) && (ext === ".js" || ext === ".css");
  })
  .sort();

// Each entry uses Liquid's asset_url to get the CDN URL and extracts the
// version query string (e.g. "?v=12345") by splitting on the filename.
const importMapEntries = chunkFiles
  .map((f, i) => {
    const comma = i < chunkFiles.length - 1 ? "," : "";
    return `    "${f}": {{ '${f}' | asset_url | split: '${f}' | last | json }}${comma}`;
  })
  .join("\n");

const snippet = `{% # theme-check-disable %}
{%- comment -%}
  IMPORTANT: This snippet is automatically generated by scripts/generate-theme-assets.js.
  Do not modify directly — it will be overwritten on next build.

  Usage:
    {%- render 'theme_assets', import_map: true -%}
    {%- render 'theme_assets' with 'main.min.js', preload: true, is_module: false -%}
    {%- render 'theme_assets' with 'main.min.css', preload: true -%}
{%- endcomment -%}
{%- if import_map -%}
  {%- assign _cdn_base = 'main.min.js' | asset_url | split: 'main.min.js' | first -%}
  <script type="application/json" data-public-path="{{ _cdn_base }}">
  {
${importMapEntries}
  }
  </script>
{%- elsif theme_assets contains '.css' -%}
  {%- if preload -%}
    <link rel="preload" href="{{ theme_assets | asset_url }}" as="style">
  {%- endif -%}
  <link rel="stylesheet" href="{{ theme_assets | asset_url }}">
{%- elsif theme_assets != blank -%}
  {%- if preload -%}
    <link rel="preload" href="{{ theme_assets | asset_url }}" as="script">
  {%- endif -%}
  {%- if is_module -%}
    <script type="module" src="{{ theme_assets | asset_url }}"></script>
  {%- else -%}
    <script src="{{ theme_assets | asset_url }}" defer></script>
  {%- endif -%}
{%- endif -%}
`;

fs.writeFileSync(path.join(snippetsDir, "theme_assets.liquid"), snippet);
console.log(`✓ Generated theme_assets.liquid with ${chunkFiles.length} chunk(s) in import map`);
